miGrupo = []

def Captura(numero):
    
    for i in range(numero):
        nombre = input("Nombre del alumno: ")
        calificacion = float(input("Calificación del alumno: "))
        miGrupo.append([nombre, calificacion])

    return miGrupo

def PromedioGrupo(lista):
    promedioAlumnos = 0
    for alumno in lista:
        promedioAlumnos += alumno[1]
    promedioFinal = promedioAlumnos / len(lista)
    return promedioFinal

def MaximaCalificación(lista):
    
    mayor = lista[0][1]
    for alumno in lista:
        calidcacion = alumno[1]
        if calidcacion > mayor:
            mayor = calidcacion
    
    return mayor
    
def ordenamiento_burbuja_por_calificacion(lista):
    lista_clonada = lista[:]
    n = len(lista_clonada)

    for i in range(n - 1):
         for j in range(0, n - i - 1):
            if lista_clonada[j][1] > lista_clonada[j + 1][1]:
                lista_clonada[j], lista_clonada[j + 1] = lista_clonada[j + 1], lista_clonada[j]

    return lista_clonada



#Numero de veces que desea capturar un alumno Puebas
ejemploAlumnos = int(input("Número entero: "))

alumnosCapturados = Captura(ejemploAlumnos)

promedioDefinitivo = PromedioGrupo(alumnosCapturados)

calificacionMayor = MaximaCalificación(alumnosCapturados)

lista_ordenada = ordenamiento_burbuja_por_calificacion(alumnosCapturados)

print(f"\nLos alumnos son: {alumnosCapturados}, donde se tien un promedio de {promedioDefinitivo:.2f},")
print(f"Con la calificacion mas alta como {calificacionMayor}")
print(f"Lista ordenada por calificación:", lista_ordenada)